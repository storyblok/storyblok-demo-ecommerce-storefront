<script setup>
const props = defineProps({
  logo: Object,
  disable_transparency: Boolean,
  auto_nav: Boolean,
  auto_nav_folder: String,
  nav: Object,
  buttons: Object,
  light: Boolean,
})

const folderStories = ref(null)

const getFolderStories = async () => {
  const storyblokApi = useStoryblokApi()
  const { data } = await storyblokApi.get('cdn/stories', {
    version: 'draft',
    level: props.auto_nav_folder ? 2 : 1,
    excluding_slugs: 'site-config,error-404',
    excluding_fields: 'body',
    starts_with: props.auto_nav_folder,
    per_page: 5,
  })
  folderStories.value = !props.auto_nav_folder
    ? data.stories.filter(
        (story) => story.parent_id === 0 || story.parent_id === null
      )
    : data.stories
}

getFolderStories()

watch(
  () => props.auto_nav_folder,
  () => getFolderStories()
)

const mobileNavOpen = ref(false)

const toggleMobileNav = () => {
  mobileNavOpen.value = !mobileNavOpen.value
}

const route = useRoute()
watch(route, () => {
  mobileNavOpen.value = false
})

const headerClasses = ref('h-32')
const logoScale = ref('scale-100')

const headerBg = computed(() => {
  return props.light ? 'bg-white' : 'bg-neutral-900'
})

const headerTransparency = computed(() => {
  return props.disable_transparency ? '' : 'bg-opacity-80 backdrop-blur-lg'
})

onMounted(() => {
  window.addEventListener('scroll', () => {
    if (window.scrollY > 60) {
      headerClasses.value = ' shadow-md h-20'
      logoScale.value = 'scale-75'
    } else {
      headerClasses.value = 'h-32'
      logoScale.value = 'scale-100'
    }
  })
})
</script>

<template>
  <header class="fixed z-[99] top-0 left-0 w-full">
    <div aria-label="Top header" class="bg-light text-dark">
      <ul
        class="max-w-[1536px] mx-auto w-full flex justify-end py-2 px-4 lg:px-8 space-x-4 lg:space-x-8"
      >
        <li>
          <a href="#">Sign In</a>
        </li>
        <li><a href="#">Create an Account</a></li>
      </ul>
    </div>
    <div
      class="transition-all duration-700"
      :class="[headerClasses, headerBg, headerTransparency]"
      aria-label="Main header"
    >
      <div
        class="max-w-[1536px] mx-auto w-full px-4 lg:px-8 h-full flex items-center justify-between lg:justify-start"
      >
        <NuxtLink to="/" class="flex shrink-0">
          <img
            :src="logo.filename"
            :alt="logo.alt"
            class="max-w-[180px] xl:max-w-[250px] max-h-[80px] w-full object-contain transition-transform origin-left duration-700 pointer-events-none"
            :class="logoScale"
          />
        </NuxtLink>
        <nav class="main-nav mx-auto invisible hidden lg:visible lg:block">
          <ul v-if="!auto_nav">
            <li v-for="item in nav" :key="item._uid">
              <NavItem
                class="hover:underline hover:underline-offset-2"
                :class="light ? 'text-primary' : 'text-white'"
                :item="item"
              />
            </li>
          </ul>
          <ul v-else>
            <li v-for="story in folderStories" :key="story.uuid">
              <NuxtLink
                :to="story.full_slug"
                class="transition-colors cursor-pointer hover:underline hover:underline-offset-2"
                :class="light ? 'text-primary' : 'text-white'"
              >
                {{ story.name }}
              </NuxtLink>
            </li>
          </ul>
        </nav>
        <HeaderCart />
        <MobileNavToggle
          @click="toggleMobileNav"
          :color="light ? 'bg-dark' : 'bg-light'"
        />
      </div>
    </div>
  </header>
  <MobileNav
    :mobileNavOpen="mobileNavOpen"
    :headerNav="nav"
    :autoNav="auto_nav"
    :folderStories="folderStories"
  />
  <pre></pre>
</template>

<style scoped>
header nav.main-nav a.router-link-active {
  @apply underline underline-offset-4 decoration-4 decoration-primary;
}

header nav.main-nav ul {
  @apply flex space-x-4 xl:space-x-8 xl:text-lg;
}
</style>
